public class Main {

    public static void main(String[] args) {
        int daysInYear = 365;
        int hoursInDay = 24;
        int secondsInHour = 3600;
        int secondsInYear = daysInYear * hoursInDay * secondsInHour;
        System.out.print("There are " + secondsInYear + " seconds in a year.");
    }
}
